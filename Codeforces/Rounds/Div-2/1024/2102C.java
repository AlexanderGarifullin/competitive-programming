    static void solve() throws IOException {
        int N = 510;
        int a[][] = new int[N][N];
        int n = cin.nextInt();
        int i = n/2;
        int j = n/2;
        if (n % 2 == 0) {
            i--;
            j--;
        }
        int id = 2;
        int t = 0;
        int val = 0;
        while(val < n * n) {
            int sz = id / 2;
            while(sz-->0) {
                a[i][j] = val++;
                if (t == 0) j++;
                else if (t == 1) i++;
                else if (t == 2) j--;
                else if (t == 3) i--;
            }
            t = (t + 1) % 4;
            id++;
        }

        for (int k = 0; k < n; k++) {
            for (int l = 0; l < n; l++) {
                System.out.print(a[k][l] + " ");
            }
            System.out.println();
        }
    }

